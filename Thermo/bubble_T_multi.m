T=fzero(A,330)-273.15

function G=bubblepoint(T)
P=4.05;
x1=0.4; x2=0.25; x3=0.2; x4=0.15;
A=[4.35576,3.9892,4.00266,4.02832];
    B=[1175.581,1070.617,1171.53,1268.636];
    C=[-2.071,-40.454,-48.784,-56.199];
    Pv1=10^(A(1)-(B(1)/(T+C(1))));Pv2=10^(A(2)-(B(2)/(T+C(2))));
    Pv3=10^(A(3)-(B(3)/(T+C(3))));Pv4=10^(A(4)-(B(4)/(T+C(4))));
    G=P-(x1*Pv1+x2*Pv2+x3*Pv3+x4*Pv4);
end
     
    